#include "KRJsUtil.h"

namespace kuikly {
namespace util {

void get_str_from_js_str(const JSVM_Env &env, const JSVM_Value &value, std::string &str) {
    size_t size;
    JSVM_Status status;
    status = OH_JSVM_GetValueStringUtf8(env, value, nullptr, 0, &size);
    if (status != JSVM_OK) {
        return;
    }
    str.resize(size + 1, 0);
    status = OH_JSVM_GetValueStringUtf8(env, value, str.data(), str.size(), &size);
    if (status == JSVM_OK) {
        str.resize(size);
    }
}
}  // namespace util
}  // namespace kuikly
